// *** if 2 ***

let result = prompt("what is your result?");
if (result>=90)
{
    document.write("excellent")
}
else if(result>=80)
{
    document.write("very good")
}
else if(result>=70)
{
    document.write("good")
}

else if(result>=50)
{
    document.write("acceptable")
}
else 
{
    document.write("fail")
}